# Tu primer script en Python

# Variables y tipos de datos
nombre = "Ana"
edad = 28
altura = 1.65
es_estudiante = True

print(f"Hola, me llamo {nombre}")
print(f"Tengo {edad} años")
print(f"Mido {altura} metros")
print(f"¿Es estudiante? {es_estudiante}")

# Operadores básicos
a = 10
b = 3
print(f"Suma: {a + b}")
print(f"Resta: {a - b}")
print(f"Multiplicación: {a * b}")
print(f"División: {a / b}")
print(f"División entera: {a // b}")
print(f"Potencia: {a ** b}")

# type() para ver el tipo de dato
print(type(nombre))
print(type(edad))
print(type(altura))
print(type(es_estudiante))
